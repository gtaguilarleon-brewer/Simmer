/**
 * detectCategory
 *
 * Assigns a grocery store category to an ingredient.
 *
 * Priority order:
 *   1. haikuCategory — if Haiku returned a valid known category, trust it.
 *   2. Keyword match on item name + qualifier (combined string).
 *   3. "Other" as final fallback.
 *
 * @param {string} itemName    - The cleaned item name (e.g., "olive oil")
 * @param {string} haikuCategory - Category returned by Haiku API (may be "")
 * @param {string} qualifier   - Qualifier string (e.g., "canned", "freshly ground")
 * @returns {string} category name
 */
export function detectCategory(itemName, haikuCategory = '', qualifier = '') {
  // 1. Trust Haiku if it returned a valid category
  if (haikuCategory && CATEGORY_ORDER.includes(haikuCategory)) {
    return haikuCategory;
  }

  // 2. Keyword match on item + qualifier combined
  const combined = `${itemName} ${qualifier || ''}`.toLowerCase().trim();
  for (const [cat, keywords] of Object.entries(CATEGORY_KEYWORDS)) {
    if (keywords.some(kw => {
      const kwLower = kw.toLowerCase();
      // Multi-word keywords: all words must appear anywhere in combined
      // (order-independent, handles "Greek yogurt" matching "yogurt Greek")
      const words = kwLower.split(/\s+/);
      if (words.length > 1) {
        return words.every(w => combined.includes(w));
      }
      // Single-word keywords: substring match (same as original behavior).
      // Word-boundary regex is too strict — it won't match "chips" for keyword "chip",
      // "berries" for keyword "berry", etc.
      return combined.includes(kwLower);
    })) {
      return cat;
    }
  }

  return 'Other';
}
